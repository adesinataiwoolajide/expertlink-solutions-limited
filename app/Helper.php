    function getDateDiff($dateOne, $dateTwo){
        $time1 = new DateTime($dateOne);
        $time2 = new DateTime($dateTwo);
        $timediff = $time1->diff($time2);
        return $timediff->format('%h hour %i minute %s second');
    }

